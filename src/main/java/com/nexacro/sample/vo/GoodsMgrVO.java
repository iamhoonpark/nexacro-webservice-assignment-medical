package com.nexacro.sample.vo;

import com.nexacro.uiadapter17.spring.core.data.DataSetRowTypeAccessor;

public class GoodsMgrVO implements DataSetRowTypeAccessor{
	private String 	goods_code; //상품코드
	private String 	goods_name; //상품명
	private String 	goods_category; // 상품 종류
	private int		goods_price; // 상품 가격
	private String	goods_pic; // 상품사진URL
	private int 	reamincnt; // 재고건수
	private int 	rowType;
	
	public String getGoods_code() {
		return goods_code;
	}
	public void setGoods_code(String goods_code) {
		this.goods_code = goods_code;
	}
	public String getGoods_name() {
		return goods_name;
	}
	public void setGoods_name(String goods_name) {
		this.goods_name = goods_name;
	}
	public String getGoods_category() {
		return goods_category;
	}
	public void setGoods_category(String goods_category) {
		this.goods_category = goods_category;
	}
	public int getGoods_price() {
		return goods_price;
	}
	public void setGoods_price(int goods_price) {
		this.goods_price = goods_price;
	}
	public String getGoods_pic() {
		return goods_pic;
	}
	public void setGoods_pic(String goods_pic) {
		this.goods_pic = goods_pic;
	}
	public int getReamincnt() {
		return reamincnt;
	}
	public void setReamincnt(int reamincnt) {
		this.reamincnt = reamincnt;
	}
	
    @Override
    public int getRowType() {
        return this.rowType;
    }

    @Override
    public void setRowType(int rowType) {
        this.rowType = rowType;
    }

	
}
