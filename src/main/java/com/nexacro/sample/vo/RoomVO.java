package com.nexacro.sample.vo;

import java.util.Date;

public class RoomVO {
	
	private String bd_code;
	private String bd_name;
	private Date bd_date;
	private String bd_text;
	private String userId;
	private String loginID;
	public String getBd_code() {
		return bd_code;
	}
	public void setBd_code(String bd_code) {
		this.bd_code = bd_code;
	}
	public String getBd_name() {
		return bd_name;
	}
	public void setBd_name(String bd_name) {
		this.bd_name = bd_name;
	}
	public Date getBd_date() {
		return bd_date;
	}
	public void setBd_date(Date bd_date) {
		this.bd_date = bd_date;
	}
	public String getBd_text() {
		return bd_text;
	}
	public void setBd_text(String bd_text) {
		this.bd_text = bd_text;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getLoginID() {
		return loginID;
	}
	public void setLoginID(String loginID) {
		this.loginID = loginID;
	}
}
